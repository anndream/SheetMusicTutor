package vanwingerdenbarrier.sheetmusictutor;

/**
 * @author Bronson VanWingerden
 *
 * Enumeration to hold the possible tone values for a given note
 * NOTE: on a standard keyboard there is no B sharp or E sharp
 */

public enum Tone {
    GSharp,
    G,
    FSharp,
    F,
    E,
    DSharp,
    D,
    CSharp,
    C,
    B,
    ASharp,
    A
}
